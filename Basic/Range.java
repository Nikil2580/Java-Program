public class Range {
    public static void main(String[] args) {

        System.out.printf("Range of Integer:   %d to %d\n\n", Integer.MIN_VALUE, Integer.MAX_VALUE);

        System.out.printf("Range of Short:     %d to %d\n\n", Short.MAX_VALUE, Short.MIN_VALUE);

        System.out.printf("Range of Byte:      %d to %d\n\n", Byte.MAX_VALUE, Byte.MIN_VALUE);

        System.out.printf("Range of Long:      %d to %d\n\n", Long.MIN_VALUE, Long.MAX_VALUE);

        System.out.println("Range of Float:     " + Float.MAX_VALUE + " to " + Float.MIN_VALUE + "\n");

        System.out.println("Range of Double:    " + Double.MAX_VALUE + " to " + Double.MIN_VALUE + "\n");

        System.out.println("Range of Character: " + Character.MAX_VALUE + " to " + Character.MIN_VALUE);
    }
}
