public class Range {
    public static void main(String[] args) {

        System.out.printf("Range of Integer: %d to %d\n", Integer.MIN_VALUE, Integer.MAX_VALUE);
        System.out.printf("Range of Character: %c to %c\n", Character.MAX_VALUE, Character.MIN_VALUE);
        System.out.println("Range of Float " + Float.MAX_VALUE + " to " + Float.MIN_VALUE);
    }
}
