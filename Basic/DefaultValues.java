public class DefaultValues {
    static int i;
    static short s;
    static byte by;
    static long l;
    static float f;
    static double d;
    static char c;
    static boolean bo;

    public static void main(String[] args) {
        System.out.println("Default value of int:     " + i + "\n");
        System.out.println("Default value of short:   " + s + "\n");
        System.out.println("Default value of byte:    " + by + "\n");
        System.out.println("Default value of long:    " + l + "\n");
        System.out.println("Default value of float:   " + f + "\n");
        System.out.println("Default value of double:  " + d + "\n");
        System.out.println("Default value of char:    " + c + "\n");
        System.out.println("Default value of boolean: " + bo + "\n");
    }
}
