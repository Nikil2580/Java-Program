public class Size {
    public static void main(String[] args) {
        System.out.println("Size of byte:   " + Byte.BYTES + "\n");

        System.out.println("Size of short:  " + Short.BYTES + "\n");

        System.out.println("Size of int:    " + Integer.BYTES + "\n");

        System.out.println("Size of long:   " + Long.BYTES + "\n");

        System.out.println("Size of float:  " + Float.BYTES + "\n");

        System.out.println("Size of double: " + Double.BYTES + "\n");

        System.out.println("Size of char:   " + Character.BYTES);

        // System.out.println("Size of Boolean:" + Boolean.BYTES);
        // C.E : Size depends on JVM so Size of Byte is not Fixed
    }
}
